class ForLoop {
    
    public static void main(String[] args) {
        // 3 ส่วน
        // ส่วนตัวแปรเริ่มต้น => ตัวนับรอบ
        // เงื่อนไข
        // การเพิ่มค่า / ลดค่า
        int count;
        for (count  = 10; count >= 0  ; count--) {
            // คำสั่งที่จะทำซ้ำ
            System.out.println(count);
            if(count ==2)break;
            
            // 1,2
        }
    }
}
